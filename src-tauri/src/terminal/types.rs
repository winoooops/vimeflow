/// Request payload for update_session_cwd command
#[derive(Debug, Deserialize)]
#[cfg_attr(test, derive(ts_rs::TS))]
#[cfg_attr(test, ts(export))]
pub struct UpdateSessionCwdRequest {
    pub id: String,
    pub cwd: String,
}
